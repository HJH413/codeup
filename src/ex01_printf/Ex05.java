package ex01_printf;
//이번에는 큰따옴표(double quotation mark)가 포함된 출력문을 연습해보자.
//
//        다음 문장을 출력하시오.
//
//        "Hello World"
//        (단, 큰따옴표도 함께 출력한다.)
public class Ex05 {
    public static void main(String[] args) {
        System.out.printf("\"Hello World\"");
    }
}
/*
참고
printf( ) 함수로 큰 따옴표 문자 " 를 출력하기 위해서는 \" 로 출력해야 한다.

예시
printf("\"");
 */